### Operadores Aritmeticos###

print(3 + 4)
print(3 - 4)
print(3 / 4)
print(3 * 4)
print(10 % 3)
print(5 // 2) #hace que la divicion acabe aproximada a un numero entero
print(3 ** 4) # nos arroja el exponente

print("Hola" *2 + str(5))
print("Hola" + "4")

my_float = 2.5 * 2
print("hola " * int(my_float))

### Operadores Comparativos###

print (3 <= 4)
print (3 >= 4)
print (3 == 4)
print (3 != 4)
print (3 > 4)
print (3 < 4)

